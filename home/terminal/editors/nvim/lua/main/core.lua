-- Adds line numbers
vim.opt.number = true;
vim.opt.relativenumber = true;

-- Adds mouse support
vim.opt.mouse = "a";

-- Sets update time
vim.opt.updatetime = 300;

-- Tabs
vim.opt.tabstop = 4;
vim.opt.shiftwidth = 4;
vim.opt.expandtab = true;

-- Stops cursor reaching bottom
vim.opt.scrolloff = 10;
